package Myperpost;

public class PerPostIncrement1 {
	public static void main(String[] args)
	{
		
		int y=5;
		int x=5;
		System.out.println("Instializa vaue of y and x is 5");
		//System.out.println("Instializa value of x is 5");
		System.out.println("y++ :"+ y++);
		System.out.println("++x :"+ ++x);  //pre increment only 1 increment
		
		int a= 9;
		int b= 9;
		
		System.out.println("Instializa vaue of a and b is 9");
		//System.out.println("Instializa value of b is 10");
		System.out.println("a-- :"+ a--);
		System.out.println("--b :"+ --b);
		
	
	}
}
